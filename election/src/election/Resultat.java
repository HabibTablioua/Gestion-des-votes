    private void displayResults() {
        int total =  getTotalVotes();
        if (total == 0){
         jLabel9.setText("");
         jLabel8.setText("");
         jLabel10.setText("");
         jLabel7.setText("");
        }else{
        try {
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/election", "root", "");
            St = Con.createStatement();

            Rs = St.executeQuery("SELECT candidat.cnom, candidat.cage, candidat.cgenre, COUNT(votetb.voteid) as voteCount FROM candidat LEFT JOIN votetb ON candidat.cid = votetb.condidatid GROUP BY candidat.cid ORDER BY voteCount DESC LIMIT 1");

            if (Rs.next()) {
            jLabel9.setText(Rs.getString("cnom"));
            jLabel8.setText(Rs.getString("cage"));
            jLabel10.setText(Rs.getString("cgenre"));
            int totalVotes = getTotalVotes(); // Implement this method to get total votes
            int winningVotes = Rs.getInt("voteCount");

            if (totalVotes > 0) {
                double percentage = (double) winningVotes / totalVotes * 100;
                jLabel7.setText(String.format("Percentage: %.2f%%", percentage));
            }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }}
    }

