    private int getTotalVotes() {
    int totalVotes = 0;
    try {
        Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/election", "root", "");
        St = Con.createStatement();

        ResultSet totalVotesResult = St.executeQuery("SELECT COUNT(*) AS totalVotes FROM votetb");

        if (totalVotesResult.next()) {
            totalVotes = totalVotesResult.getInt("totalVotes");
        }
    } catch (Exception e) {
        e.printStackTrace();
    } finally {
        try {
            if (St != null) {
                St.close();
            }
            if (Con != null) {
                Con.close();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    return totalVotes;
    }

